#Задача №1
# Запросите у пользователя число и выведите его в обратном порядке. Используйте цикл while для извлечения цифр и построения обратного числа.

# Запрашиваем у пользователя ввод числа
number = int(input("Введите число: "))

# Инициализируем переменную для хранения обратного числа
reverse_number = 0

# Используем цикл while для извлечения цифр из числа
while number > 0:
    reverse_number = reverse_number * 10 + (number % 10)
    number = number // 10  # Убираем последнюю цифру

# Выводим обратное число
print(reverse_number)

#Задача №2
#Напишите программу, которая запрашивает у пользователя положительное целое число и вычисляет сумму всех его цифр.

# Запрашиваем у пользователя ввод положительного целого числа
number = int(input("Введите положительное целое число: "))

# Инициализируем переменную для хранения суммы цифр
sum_of_digits = 0

# Цикл, который извлекает и суммирует цифры числа
while number > 0:
    sum_of_digits += number % 10  # Извлекаем последнюю цифру и добавляем к сумме
    number = number // 10  # Убираем последнюю цифру

# Выводим результат
print("Сумма цифр числа:", sum_of_digits)

#Задача №3
#Запросите у пользователя число N. Программа должна выводить числа Фибоначчи до тех пор пока текущее число в последовательности меньше или равно N.

# Запрашиваем у пользователя ввод числа N
N = int(input("Введите число N: "))

# Инициализация первых двух чисел последовательности Фибоначчи
a, b = 0, 1

# Выводим числа Фибоначчи до тех пор, пока текущее число не больше N
while a <= N:
    print(a, end=" ")  # Выводим текущее число
    a, b = b, a + b  # Обновляем a и b для следующего числа в последовательности

# Задача #4
#Запросите у пользователя число. Переверните это число полностью.

# Запрашиваем у пользователя ввод числа
number = int(input("Введите число: "))

# Инициализируем переменную для перевернутого числа
reversed_number = 0

# Используем цикл while, чтобы извлечь цифры и построить перевернутое число
while number > 0:
    digit = number % 10  # Получаем последнюю цифру числа
    reversed_number = reversed_number * 10 + digit  # Добавляем цифру к перевернутому числу
    number = number // 10  # Убираем последнюю цифру из исходного числа

# Выводим перевернутое число
print("Перевернутое число:", reversed_number)

